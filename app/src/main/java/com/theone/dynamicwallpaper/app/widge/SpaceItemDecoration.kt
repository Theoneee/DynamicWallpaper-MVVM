class SpaceItemDecoration(
    private val space: Int
) :
    RecyclerView.ItemDecoration() {

    override fun getItemOffsets(
        outRect: Rect,
        view: View,
        parent: RecyclerView,
        state: RecyclerView.State
    ) {
            with(outRect) {
                left = space
                right = space
                bottom = space
                top =  space
            }
    }

}